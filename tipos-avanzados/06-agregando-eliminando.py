mascotas = [
    "Wolfgang",
    "Pelusa",
    "Pulga",
    "Felipe",
    "Pulga",
    "Chanchito feliz"
]
print(mascotas)
# Inserta un elemento segun criterios que se adiciones en los argumentos (Indice, Adicion)
mascotas.insert(1, "Melvin")
print(mascotas)
# Adiciona un elemento al final de la lista
mascotas.append("Chanchito triste")
print(mascotas)
# Borra el elemento que menciones en los argumentos
mascotas.remove("Pulga")
print(mascotas)
# Borra el ultimo elemento si no se especifica en los argumentos cual quieres retirar
mascotas.pop(1)
print(mascotas)
# Elimina el elemeneto seleccionado en los argumentos
del mascotas[0]
print(mascotas)
# Limpia en su totalidad la lista
mascotas.clear()
print(mascotas)
